package com.test;

import org.springframework.context.ApplicationContext;
import org.springframework.context.support.ClassPathXmlApplicationContext;

import com.frame.Service;
import com.vo.ProductVO;

public class ProductInsert {

	public static void main(String[] args) {
		System.out.println("Spring Start ..");
		
		//Spring 환경이 만들어진다. 
		ApplicationContext factory =
		new ClassPathXmlApplicationContext("spring.xml");

		
		//IOC (Inversion Of Control) 제어 역행  -> 원래 방향은 ( Service s = new UserService())
		//XML 파일을 이용해 객체를 생성 -> 생성하며 DI 의존성 주입이 일어난다. 
		Service<String, ProductVO> service = (Service<String, ProductVO>) factory.getBean("pservice");
		
		ProductVO u = new ProductVO("pants05",30000,11);//database의 제약조건이 걸려있어서 11,21밖에 안들어감 
		
		try {
			service.register(u);
		} catch (Exception e) {
			System.out.println("insert 입력 오류가 발생했습니다. ");
			//e.printStackTrace();
		}
		System.out.println("Spring End ..");

	}

}
